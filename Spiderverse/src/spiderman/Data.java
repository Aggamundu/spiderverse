package spiderman;

public class Data {
  private int number;
private int canon;
private int weight;

public void setData (int number, int canon, int weight){
this.number = number;
this.canon = canon;
this.weight = weight;
}

public int getNumber(){
return number;
}
public int getCanon(){
return canon;
}
public int getWeight(){
  return weight;
}

}



